// 최대값
console.log(Math.max(1, 2, 3, 4, 5)); // 5

// 최소값
console.log(Math.min(1, 2, 3, 4, 5)); // 1

// 절대값
console.log(Math.abs(-10)); // 10

// 반올림 - 자릿수 지정할 수 없음
console.log(Math.round(25.3533)); // 25

// 반올림
console.log((23.3533).toFixed(2)); // 23.35 - 소숫점 2째짜리 까지 반올림

// 올림
console.log(Math.ceil(23.3533)); // 24

// 버림
console.log(Math.floor(23.3533)); // 23

// 난수 0.0 이상 ~ 1.0 미만
console.log(Math.random()); // 매 실행시마다 바뀜

// +@
console.log((25.523).toFixed(2)); // 25.52
console.log((25.526).toFixed(2)); // 25.53 (반올림)
